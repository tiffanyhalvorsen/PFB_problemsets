#!/usr/bin/env python3

setA = {3,14,15,9,26,5,35,9}
setB = {60,22,14,0,9}

print(f'intersection of A and B is: {setA & setB}')
print(f'difference between A and B is: {setA - setB}')
print(f'union between A and B is: {setA | setB}')
print(f'the symmetric difference between A and B is: {setA ^ setB}')

